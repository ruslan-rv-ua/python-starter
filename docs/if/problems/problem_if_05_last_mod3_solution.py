'''
"Остання на 3"

Напишіть програму яка виконує наступне:
1. Просить користувача ввести ціле число
2. Якщо остання цифра цього числа ділиться націло на 3, програма виводить "Мені підходить"
3. Інакше програма виводить "Мені не підходить"
'''
# ваш код починається тут
n = int(input('Enter integer '))
if n < 0:
	n = -n
if n % 10 % 3 == 0:
	print('ok')
else:
	print('not ok')
