COLUMNS = 'abcdefgh'
ROWS = '12345678'

def is_cell_valid(cell):
	if len(cell) != 2:
		return False
	return cell[0].lower() in COLUMNS and \
		cell[1].lower() in ROWS

def get_user_input():
	while True:
		cell = input('Введіть номер клітинки: ')
		if is_cell_valid(cell):
			return cell
		print('Неправильний ввід')
	
def get_cell_color(cell):
	assert is_cell_valid(cell), f'Incorrect cell: {cell}'
	x = COLUMNS.index(cell[0].lower())
	y = ROWS.index(cell[1].lower())
	return 'чорний' if (x + y) % 2 == 0 else 'білий'

cell = get_user_input()
color = get_cell_color(cell)
print(f'Колір клітинки {cell}: {color}')


