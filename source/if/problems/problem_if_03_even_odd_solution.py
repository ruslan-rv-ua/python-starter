'''
"Парне-непарне"

Напишіть програму яка виконує наступне:
1. Просить ввести користувача ціле число
2. Повідомляє користувача чи є це число парним чи непарним.

Скористайтесь тернарним if-else.

Сценарій роботи програми такий:
Введіть ціле число: 12
Число 12 парне
'''
# ваш код починається тут
number = int(input('Введіть ціле число: '))
result = 'парне' if number % 2 == 0 else 'непарне'
print('Число', number, result)