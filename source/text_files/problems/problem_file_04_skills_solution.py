result = []
in_file = open('skills.txt', 'r', encoding = 'UTF-8')
for line in in_file:
	data = line.strip().split()
	name = f'{data.pop(2)} {data.pop(0)[0]}.{data.pop(0)[0]}.'
	marks = []
	for m in data:
		marks.append(int(m))
	avg = int(sum(marks) / len(marks) * 100) / 100
	text_line = f'{name},{avg}'
	result.append(text_line)
in_file.close()

result.sort()
out_file = open('skills.csv', 'w', encoding = 'UTF-8')
out_file.write('\n'.join(result))
out_file.close()

